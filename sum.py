# Определение списка чисел(ВВЕДИТЕ НУЖНЫЕ ВАМ ЗНАЧЕНИЯ ЧЕРЕЗ ЗАПЯТУЮ)
numbers = [11,22,33,44,55]  # Пример списка

# Функция для подсчета суммы чисел в списке
def calculate_sum(number_list):
    return sum(number_list)

# Вычисление суммы и вывод результата
total_sum = calculate_sum(numbers)
#Вывод значения
print("Сумма всех чисел в списке:",total_sum)